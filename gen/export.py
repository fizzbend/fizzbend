from docx import Document
import io

def create_world_docx(world):
    """
    Takes the World object and generates a formatted Word document.
    Returns an in-memory byte buffer to be downloaded by the user.
    """
    doc = Document()
    
    # 1. Title Page / Header
    doc.add_heading(f"The Chronicles of Seed: {world.seed}", 0)
    doc.add_paragraph("Generated by Fizzbend Games World Engine")
    
    # 2. World State Summary
    doc.add_heading("The State of the World", level=1)
    state_text = (
        f"Years Recorded: {world.year}\n"
        f"Climate: {world.climate} | Size: {world.size}\n"
        f"Stability: {world.stability} | Magic: {world.magic_level} | Tech: {world.tech_level}"
    )
    doc.add_paragraph(state_text)
    
    # 3. Factions & Demographics
    doc.add_heading("Known Factions", level=1)
    for faction in world.factions.values():
        status = "Majority" if faction.is_majority else "Minority"
        doc.add_paragraph(
            f"{faction.race_name} ({status}) - Population: {faction.population} | Tech Mastery: {faction.local_tech}", 
            style='List Bullet'
        )

    # 4. The Historical Ledger
    doc.add_heading("The Ledger of Ages", level=1)
    for event in world.history_log:
        doc.add_heading(f"Year {event.year}: {event.title}", level=2)
        doc.add_paragraph(event.description)
        
        # Add small impact notes (e.g., Stability: -5)
        if hasattr(event, 'impact_vars') and event.impact_vars:
            impact_str = ", ".join([f"{k.capitalize()}: {v:+d}" for k, v in event.impact_vars.items()])
            p = doc.add_paragraph(f"World Shifts: {impact_str}")
            p.style = 'Intense Quote' # Gives it a stylized look in Word

    # 5. Save to Memory Buffer (No hard drive space used!)
    buffer = io.BytesIO()
    doc.save(buffer)
    buffer.seek(0)
    
    return buffer